import { createBrowserRouter, Outlet, RouterProvider } from "react-router-dom";
import React from "react";
import { useState, useEffect } from "react";

import Navbar from "./components/navbar/Navbar";
import Footer from "./components/footer/Footer";

import "./App.scss";

import {
  Add,
  Gig,
  Gigs,
  Home,
  Message,
  Messages,
  MyGigs,
  Orders,
} from "./pages/";

function App() {
  const Layout = () => {
    return (
      <div className="app">
        <Navbar />
        <Outlet />
        <Footer />
      </div>
    );
  };

  const router = createBrowserRouter([
    {
      path: "/",
      element: <Layout />,
      children: [
        {
          path: "/",
          element: <Home />,
        },
        {
          path: "/gigs",
          element: <Gigs />,
        },
        {
          path: "/myGigs",
          element: <MyGigs />,
        },
        {
          path: "/orders",
          element: <Orders />,
        },
        {
          path: "/messages",
          element: <Messages />,
        },
        {
          path: "/message/:id",
          element: <Message />,
        },
        {
          path: "/add",
          element: <Add />,
        },
        {
          path: "/gig/:id",
          element: <Gig />,
        },
      ],
    },
  ]);

  return (
    <div>
      <RouterProvider router={router} />
    </div>
  );
}

export default App;
